# ╔═╡ 83629869-c3af-4d78-97a1-1bcbc6941395
function parse_input(data)
    idxs = Dict{String,Int}()
    g = SimpleGraph()
    for line in split(data, '\n')
        from, tos = split(line, ": ")
        from_idx = get!(idxs, from) do
            add_vertex!(g)
            length(idxs) + 1
        end
        for to in split(tos)
            to_idx = get!(idxs, to) do
                add_vertex!(g)
                length(idxs) + 1
            end
            add_edge!(g, from_idx, to_idx)
        end
    end
    g, idxs
end

# ╔═╡ c3779275-c2c5-476e-9619-fb10b1b212c7
function karger_min_cut_of_3(graph)
    groups = Int[]
    while true
        groups = karger_min_cut(graph)
        length(karger_cut_edges(graph, groups)) == 3 && break
    end
    groups
end

# ╔═╡ a8c60bdf-e798-4630-a3ca-263d3040feb9
function answer1(input)
    graph, vertices = input
    # mincut apears to be broken
    # https://github.com/JuliaGraphs/Graphs.jl/issues/324
    #group, count = mincut(graph)
    group = karger_min_cut_of_3(graph)
    sum(group .== 1) * sum(group .== 2)
end

